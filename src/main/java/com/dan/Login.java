    private void jbtLoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtLoginActionPerformed
        // TODO add your handling code here:
        String account = jtfAccount.getText();
        String password = String.valueOf(jtfPassword.getPassword());
        
        if (UserService.checkLogin(account, password)) {
            openProductTable(account);
        }
        else {
            jlbError.setText("Tài khoản hoặc mật khẩu không đúng");
        }
    }//GEN-LAST:event_jbtLoginActionPerformed

