    private void openProductTable(String account) {
        ProductTable productTable = new ProductTable(account);
        User user = UserService.getUser(account);
        productTable.setVisible(true);
        this.dispose();
        JOptionPane.showMessageDialog(null, "Xin chào "+user.getName());
    }

